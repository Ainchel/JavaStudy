//클래스와 메소드에 final을 붙일 수 있는데, 이 때의 변화는 상속과 관계가 있다.

//final이 붙은 클래스는, 더이상 상속할 수 없는(부모 클래스가 될수 없는) 클래스가 된다.
//우리는 모르지만, String 클래스가 final로 되어 있으며 다른 클래스로 String을 상속할 수 없다.

